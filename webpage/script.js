async function getThingSpeakData() {
    try {
        const response = await fetch(thingSpeakApiUrl);
        const data = await response.json();
        updateThingSpeakUI(data);
    } catch (error) {
        console.error('Error fetching ThingSpeak data:', error);
    }
}
function updateThingSpeakUI(data) {
    cityName.textContent = cityInput.value.trim();
    date.textContent = new Date().toLocaleDateString();
    temp.textContent = data.feeds[0].field1 ? `${data.feeds[0].field1}°C` : 'N/A';
    rainStatus.textContent = data.feeds[0].field3 ? (data.feeds[0].field3 == 1 ? 'Raining' : 'No Rain') : 'N/A';
    rainIntensity.textContent = data.feeds[0].field4 ? `${data.feeds[0].field4}` : 'N/A';
    humidity.textContent = data.feeds[0].field2 ? `${data.feeds[0].field2}%` : 'N/A';
    pressure.textContent = data.feeds[0].field7 ? `${data.feeds[0].field7} M/B` : 'N/A'; 
    gasLevel.textContent = data.feeds[0].field5 ? `${data.feeds[0].field5} ppm` : 'N/A';  
    disaster_Index.textContent = data.feeds[0].field8 ? `${data.feeds[0].field8}` : 'N/A';
    let alert_message = "";
    let disaster_message = "";
    const TEMP_THRESHOLD = 45;
    const HUMIDITY_THRESHOLD = 80;
    const GAS_THRESHOLD = 80;
    const SOIL_MOISTURE_LOW = 30;
    const RAIN_INTENSITY = 0;
    const disaster_index = parseInt(data.feeds[0].field8) || 0;

    if (data.feeds[0].field1 > TEMP_THRESHOLD) {
        alert_message = "⚠️ Extreme Heat Warning! \n";
    }
    if (data.feeds[0].field2 > HUMIDITY_THRESHOLD) {
        alert_message += "⚠️ High Humidity Levels! \n";
    }
    if (data.feeds[0].field3 == 1) {
        alert_message += "⚠️ Rain Detected! ☔ Stay alert for flooding!\n";
    }
    if (data.feeds[0].field3 == 1 & rainIntensity > RAIN_INTENSITY ) {
        alert_message += "☔ Stay alert for flooding!\n";
    }
    if (data.feeds[0].field5 > GAS_THRESHOLD) {
        alert_message += "⚠️ High Gas Levels Detected! ️ Possible air contamination!\n";
    }
    if (data.feeds[0].field6 < SOIL_MOISTURE_LOW) {
        alert_message += "⚠️ Low Soil Moisture!  Possible drought risk.\n";
    }
    document.getElementById("alert_message").innerHTML = alert_message;
    
    if (disaster_index > 70) {
        disaster_message = " Severe Disaster Warning! Possible Floods or Landslides! ⛰️\n";
    } else if (disaster_index > 50) {
        disaster_message = "⚠️ Moderate Disaster Warning! Potential Storms or Heavy Rain!\n";
    } else if (disaster_index > 30) {
        disaster_message = "⚠️ Mild Disaster Risk! Monitor weather conditions!\n";
    } else {
        disaster_message = "✅ No major disaster risks detected. Stay safe!\n";
    }
    document.getElementById("disaster_message").innerHTML = disaster_message;
}
